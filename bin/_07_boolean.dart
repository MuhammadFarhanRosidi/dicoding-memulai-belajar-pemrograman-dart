void main() {
  bool isTrue = true;
  bool isFalse = false;
  bool notTrue = !isTrue;
  bool notFalse = !isFalse;
  // symbol ! is used to negate the value of a boolean variable called not operator or logical negation operator or logical complement operator or logical NOT operator or logical inversion operator or logical reverse operator or logical flip-flop operator or logical bar operator or logical bang operator or logical exclamation operator or logical exclamation mark operator

  print(isTrue);
  print(isFalse);
  print(notTrue);
  print(notFalse);

  if (isTrue) {
    print('true');
  } else {
    print('false');
  }
}
